package net.csa.conference.model;

import java.util.Date;

/**
 * Created by mike on 30.11.16.
 */
public class TimeSpan {
    private Date begin;
    private Date end;

    public Date getBegin() {
        return begin;
    }

    public void setBegin(Date begin) {
        this.begin = begin;
    }

    public Date getEnd() {
        return end;
    }

    public void setEnd(Date end) {
        this.end = end;
    }
}
